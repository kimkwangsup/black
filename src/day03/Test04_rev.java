package day03;

public class Test04_rev {
	public static void main(String[] args) {
		/*
		 	반전연산자
		 	: 숫자의 부호를 반전시켜주는 연산자.
		 		형식 ] 
		 			-숫자
		 */
		int no = 10;
		System.out.println("no : " + (-no));
		
		/*
		 	부정연산자
		 	: 논리값을 반대로 만들어주는 연산자.
		 		형식 ]
		 			!논리값
		 */
		boolean bool = false;
		System.out.println("bool : " + !bool);
	}
}
